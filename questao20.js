/*
Como você pode chamar uma função de retorno de chamada em 
JavaScript?
*/
// Definição da função de retorno de chamada
function minhaFuncaoDeRetorno(argumento1, argumento2) {
    console.log("A função de retorno de chamada foi chamada!");
    console.log("Argumento 1:", argumento1);
    console.log("Argumento 2:", argumento2);
  }
  
  // Chamada da função de retorno de chamada
  minhaFuncaoDeRetorno("valor1", "valor2");