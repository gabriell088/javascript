/*
Como você pode acessar o valor de uma propriedade de um 
objeto em JavaScript?
*/
let meuObjeto = {
    propriedade: "valor"
  };
  
  // Acessando o valor da propriedade usando a notação de ponto
  let valorDaPropriedade = meuObjeto.propriedade;
  
  console.log(valorDaPropriedade); // Saída: "valor"